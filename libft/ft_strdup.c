#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*dup;
	int		i_s1;
	int		i;

	i = 0;
	i_s1 = ft_strlen(s1);
	dup = (char *)malloc(sizeof(*dup) * (i_s1 + 1));
	if (dup == NULL)
		return (NULL);
	while (s1[i] != '\0')
	{
		dup[i] = s1[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
